#include <unistd.h>

int	ft_strlen(char *str)
{
	int	len;

	len = 0;
	while (str[len])
		len++;
	return (len);
}

int	main(int argc, char **argv)
{
	int	i;

	i = 1;
	while (i < argc)
	{
		write(1, argv[i], ft_strlen(argv[i]));
		write(1, "\n", 1);
		i++;
	}
	return (0);
}

// i, argv[1]'den başlar.
// Döngü argc'ye kadar sürer.
// Her argüman write ile yazılır.
// Ardından yeni satır eklenir.
// argv[0] atlanır.
