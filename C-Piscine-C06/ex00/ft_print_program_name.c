#include <unistd.h>

static int	ft_strlen(char *str)
{
	int	len;

	len = 0;
	while (str[len])
		len++;
	return (len);
}

int	main(int argc, char **argv)
{
	if (argc > 0)
	{
		write(1, argv[0], ft_strlen(argv[0]));
		write(1, "\n", 1);
	}
	return (0);
}

// ft_strlen uzunluk hesaplar.
// argv[0] program adını tutar.
// write ile ekrana yazılır.
// Ardından yeni satır yazılır.
